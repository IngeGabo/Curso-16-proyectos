# Variables: Son espacios de memoria que se utilizan para almacenar valores.
# Python es un lenguaje de tipado dinámico, por lo que no es necesario declarar el tipo de variable.

# Ejemplo de variables
# Ejemplo 1: Variables con un string,
nombre = "Juan"
print(nombre)
# Ejemplo 2: Cambio de valor de la variable nombre
nombre = "Pedro"
print(nombre)
# Ejemplo 3: Variables con un número entero
edad = 20
print(edad)
# Ejemplo 4: Variables con un número que sera sumado con otro número en un print
edad2 = 30
print(edad + edad2)
# Ejemplo 5: Almacenamos un valor dentro de la variable 
nombre = input("Ingrese su nombre: ")
print(nombre)
# Ejemplo 6: Concatenación de variables
nombre = "Hola"
nombre2 = "Python"
frase = nombre + " " + nombre2
print(frase)
# Ejemplo 7: Concatenación de variables con un número
nombre = 30
nombre2 = 40
frase = nombre + nombre2
print(frase)

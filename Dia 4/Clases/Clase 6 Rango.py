# Rango: Es una función que genera una secuencia de números de acuerdo a un rango dado.

# Sintaxis:
# range(inicio, fin, salto)

# Ejemplo 1
for i in range(5):
    print(i)

# Ejemplo 2
for i in range(2, 5):
    print(i)

# Ejemplo 3
for i in range(2, 10, 2):
    print(i)

# Ejemplo 4
for i in range(10, 2, -2):
    print(i)

# Ejemplo 5
for i in range(10, 2, 2):
    print(i)

# Ejemplo 6
lista = list(range(5))
print(lista)
